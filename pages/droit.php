            <h2 class="heading-stylized">Les modalités de la création d'une entreprise</h2>
            <h3>Définition et début dans une entreprise</h3>
            <p>Le deuxième consiste à créer une entreprise qui est une organisation ou une unité institutionnelle, mue
                par un projet décliné en stratégie, en politiques et en plans d'action, dont le but est de produire et
                de fournir des biens ou des services à destination d'un ensemble de clients ou d'usagers, en réalisant
                un équilibre de ses comptes de charges et de produits. Pour, commencer, la personne vas créer une micro
                entreprise personnelle ( biens personnels et bien de société confondus) et ceci en seulement quelques
                clics. Celle-ci permet une comptabilité simplifiée avec comme simple obligation de déclarer son revenu
                mensuel ou trimestriel et est souvent utilisé pour les personnes commençant une activité et ne sachant
                pas si celle-ci va fonctionner. Si le chiffre d’affaire dépasse 82200 euros hors taxe pour l’achat
                revente ou 32900 hors taxes pour les prestations de services, il faudra créer une société.</p>
            <h3>Attribution de statut</h3>
            <p>Si l'on veut créer une société, il faudra obligatoirement lui attribuer un statut. Chaque entreprise
                possède un statut qui lui est attribué en fonction de nombreux facteurs. Ce statut détermine les règles
                de fonctionnement de l’entreprise ainsi que les droits et les obligations des personnes qui la
                composent. Voici les différents statuts que l’on peut choisir.</p>
            <h4>EUR/SASU</h4>
            <p>Une personne seule peut créer une entreprise unipersonnelle à responsabilité limitée (EUR) dans lequel le
                PDG est non salarié et qui comporte plusieurs avantages, une création facile en ligne et capital maximal
                exigé de 1 euro ou bien une Société par Action Simplifiée Unipersonnelle (SASU) dans lequel le PDG est
                salarié (même statut sociale que les salariés) et on ne peut avoir qu’un seul associé au maximum,
                l’avantage de celle-ci est que l’on peut cesser ses actions facilement.</p>
            <h4>SARL/SAS/SA</h4>
            <p>Une association de deux personnes minimum peuvent créer une société à responsabilité limitée (SARL) dans
                laquelle les règles de fonctionnement seront fixés par la loi qui sont impossibles à modifier (par
                exemple l’entrée d’un nouvel associé dans l’entreprise doit être accordé par la majorité des autres
                associés) et le/les chefs ne seront pas salarié, ce type d’entreprise ressemble beaucoup à une EUR.</p>
            <p>Pour un fonctionnement plus souple il faut transformer cette SARL en une Société par action simplifiée
                (SAS) qui à pour avantage de favoriser l’entrée d’investisseurs extérieur dans l’entreprise (intéressant
                si l’entreprise à besoin de fonds par exemple pour développer son activité à l’étranger ou autre)
                cependant celle-ci offre des privilèges aux investisseurs comme le fait qu’en cas de faillite, ils
                récupèrent l’argent investi. Dans ce statut, le/les chefs sont salariés.</p>
            <p>Si le capital de l’entreprise atteint 37 000 euros, on peut créer une SA c'est-à-dire une Société Anonyme
                qui permet de vendre des actions en bourse, dans notre cas qui concerne les sites d’échanges, le statut
                de l'entreprise va dépendre du nombre d’associés qui sont derrière ce site, de la somme qu’il ou qu’ils
                rapportent et de leurs ambitions, par exemple, s'ils ont prévu de s’étendre à l’étranger ou de changer
